    '        print(f"\\n── Scene {num:02d}/{len(scenes)}  [{stitle}] ──")\n',
    '\n',
    '        # Step 1 — Image\n',
    '        if args.skip_images and img_path.exists():\n',
    '            print(f"   [IMG]  Reusing cached {img_path.name}")\n',
