    '            else:\n',
    '                print(f"   [IMG]  Pollinations Flux…")\n',
    '                ok = fetch_image(prompt, img_path, seed=num * 13 + idx,\n',
    '                                 channel=channel, w=w, h=h)\n',
    '                if not ok:\n',
    '                    print(f"   [IMG]  Fallback card (all fetches failed)")\n',
    '                    make_fallback_card(img_path, w, h, bg)\n',
    '\n',
    '        # Step 2 — TTS narration\n',
    '        if not audio_path.exists() or audio_path.stat().st_size < 1000:\n',
    '            print(f"   [TTS]  Generating narration…")\n',
    '            try:\n',
    '                import asyncio, edge_tts\n',
    '                async def _tts() -> None:\n',
    '                    comm = edge_tts.Communicate(narr, voice=voice, rate=rate)\n',
    '                    await comm.save(str(audio_path))\n',
    '                asyncio.run(_tts())\n',
    '                print(f"   [TTS]  ✓ {audio_path.name}")\n',
    '            except ImportError:\n',
    '                print("   [TTS]  edge-tts not installed — trying SAPI fallback…")\n',
    '                try:\n',
    '                    wav_str = str(audio_path.with_suffix(".wav"))\n',
    "                    safe_narr = narr.replace(\"'\", \"\")\n",
    '                    subprocess.run(\n',
    '                        ["powershell", "-Command",\n',
